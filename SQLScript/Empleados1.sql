-- CREACION DE LA TABLA EMPLEADOS
-- ALMACENA INFORMACION BASICA DE LOS EMPLEADOS

CREATE TABLE EMPLEADOS (

    -- ID UNICO GENERADO AUTOMATICAMENTE AUTOCINCREMENTO
    ID_EMPLEADO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,

    -- NOMBRE DEL EMPLEADO (OBLIGATORIO)
    NOMBRE VARCHAR2(100 CHAR) NOT NULL,

    -- PUESTO DEL EMPLEADO (OBLIGATORIO)
    PUESTO VARCHAR2(100 CHAR) NOT NULL,

    -- SALARIO DEL EMPLEADO
    -- HASTA 10 DIGITOS Y 2 DECIMALES
    SALARIO NUMBER(10,2) NOT NULL
);

-- RESTRICCION PARA VALIDAR SALARIOS POSITIVOS
ALTER TABLE EMPLEADOS
ADD CONSTRAINT CHK_SALARIO_POSITIVO
CHECK (SALARIO > 0);

-- INDICE PARA BUSQUEDAS POR NOMBRE
CREATE INDEX IDX_EMPLEADOS_NOMBRE
ON EMPLEADOS(NOMBRE);

-- INDICE PARA BUSQUEDAS POR PUESTO
CREATE INDEX IDX_EMPLEADOS_PUESTO
ON EMPLEADOS(PUESTO);

-- COLUMNA PARA CONTROLAR SI EL EMPLEADO ESTA ACTIVO
ALTER TABLE EMPLEADOS
ADD ACTIVO NUMBER(1) DEFAULT 1 NOT NULL;

-- FECHA DE CREACION DEL REGISTRO --AGREGA LA COLUMNA FECHA DE CREACION  
